// -- XLSX -------------------------------------------------------------------

export async function executeXlsx(ctx: DocumentExecutionContext): Promise<DocumentResult> {
  const t = Date.now();
  try {
    const { generateExcel } = await import("../../services/documentGenerators/excelGenerator");
    const title = extractTitle(ctx.userMessage);
    const headers = extractSections(ctx.userMessage).slice(0, 5);
    const h = headers.length > 0 ? headers : ["Column A", "Column B", "Column C"];
    const content = {
      sheetName: "Data", title, headers: h,
      rows: [h.map((_, i) => `Sample ${i + 1}`), h.map((_, i) => `Data ${i + 1}`)],
    };
    const r = await generateExcel(content);
    const qa = await runVisualQA(r.buffer, "xlsx");
    return toResult(r.buffer, r.filename, "xlsx", ctx, qa, Date.now() - t);
  } catch (err) {
    return toResult(Buffer.alloc(0), "error.xlsx", "xlsx", ctx, failQa("xlsx", err, Date.now() - t), Date.now() - t);
  }
}

